                            notified at time of load tender or any time after, that penalties will be in effect and the specific
                            amounts of any penalties for late delivery or missed appointment as agreed to in the load sheet,
                            excluding acts of God, terrorism, war, accidents, breakdown, or any circumstances beyond
                            CARRIER’S control. Such damages may be deducted and retained out of monies’ otherwise due to
                            CARRIER, or if not deducted, CARRIER will pay such amount upon demand from BROKER
                        </li>
