                            Agreement. Solicitation prohibited under this Agreement means participation in any conduct,
                            whether direct or indirect, the purpose of which involves transportation or handling of property
                            by CARRIER for which CARRIER does, or did in the past, provide such services for that customer
                            under arrangements first made or procured by AAA. If CARRIER performs services of a
                            transportation nature for compensation for any customer of AAA without prior written authorization
                            from AAA, CARRIER shall immediately pay to AAA an amount equal to twenty (20) percent of all gross
                            revenues invoiced by CARRIER to the solicited customer for a period of eighteen (18) months
                            following the first independent service of customer by CARRIER. AAA shall identify its
                            customers to CARRIER as each first load from each customer is tendered to CARRIER. CARRIER'S
                            acceptance of the load and movement of the property will acknowledge that this new customer
                            is AAA'S customer. This provision will survive the termination of this Agreement.
                        </li>
