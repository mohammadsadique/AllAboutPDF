                            that it will not use equipment to haul food or food products that had just previously or prior to 
                            being used to transport refuge, garbage, trash or solid liquid waste of any kind whatsoever,
                            whether hazardous or non-hazardous. CARRIER agrees that it will use only tractors, trailers and
                            other equipment which are in good condition and are clean, sound and free from odors,
                            moisture and other conditions that might result in loss or damage to or adverse effect upon the
                            goods transported. CARRIER and AAA agree that each shipment to be transported under this
                            Agreement will be evidenced by a separate Load Sheet, which Load Sheet will become a part of
                            this Agreement when CARRIER accepts a shipment and commences the movement of freight.
                            CARRIER agrees to use all of its own equipment and employees in the performance of services.
                            CARRIER is responsible for obtaining all employment benefits, certifications, and insurance, including
                            workers compensation insurance, as necessary, for operation of its business.

                        </li>
