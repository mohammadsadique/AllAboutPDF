                            and warrants that there are no other applicable rates or charges except those established in
                            this Agreement or in any Load Sheet prepared by AAA. CARRIER must provide a completed
                            invoice together with all of the signed original bill(s) of lading, signed pickup receipts and signed
                            proof of delivery receipt(s) for each shipment included on the invoice. AAA shall remit payment
                            to CARRIER for each load tendered within thirty (30) days of receipt by AAA of all properly
                            executed paperwork for the load, as determined solely by AAA. AAA is responsible for all
                            payments to CARRIER for all services rendered by the CARRIER. CARRIER shall not look to SHIPPER
                            for payment and SHIPPER's payments for services rendered will always be made directly to
                            AAA. CARRIER must maintain contact with AAA at all times during which a load is being
                            transported; a copy of its operating authority showing it to be a contract carrier, certificates of
                            insurance in effect as of the date of each shipment, all forms required by the IRS and a signed
                            Carrier-Broker Agreement. Failure to provide these documents will result in delays in the
                            payment of all invoices from the CARRIER.

                        </li>
                        <li>
                            <span class="text-highlight">Effective Date and Term</span>: This Agreement shall go into effect on the day and year herein above
                            written and remain in effect for a period of one (1) year from date hereof, and shall renew
                            automatically for one (1) year periods thereafter, subject to the right of either party hereto to
                            cancel or terminate the Agreement upon not less than thirty (30) days written notice of one
                            party to the other prior to the end of the then current term, with notice being effective upon
                            delivery.
                        </li>
                        <li>
                            <span class="text-highlight">Confidentiality</span>: CARRIER agrees that it will not divulge to any third party (a) the terms of this
