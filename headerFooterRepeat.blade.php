                            Agreement or in failing to comply with any law(s) and regulation(s) with respect to the maintenance,
                            operations and use of motor vehicles and equipment hereunder.
                        </li>
                        <li>
                            <span class="text-highlight">Conflicts:</span>:  In the event there is a conflict between the terms of this Agreement and the bill of lading,
                            the provisions of this Agreement shall govern.
                        </li>
                        <li>
                            <span class="text-highlight">Governmental Regulations</span>: AAA and CARRIER hereby mutually agree and stipulate that each
                            is familiar with all governmental regulations, that each will fully comply with said regulations,
                            and that said duty of compliance shall at all times during the term of this Agreement be a
                            material provision and obligation of each to the other. Consistent with said regulatory
                            requirements, CARRIER agrees to provide AAA all documents and information which AAA is
                            required to have on file and which are normally within the possession of CARRIER.
                        </li>
                        <li>
                            <span class="text-highlight">Notice</span>: All notices required by or related to this Agreement shall be in writing, shall be deemed
                            to have been given when sent, and shall be delivered personally or sent U.S. registered or
                            certified mail, return receipt requested, postage pre-paid, addressed as shown in the first
                            paragraph hereof to the above referenced address.
                        </li>
