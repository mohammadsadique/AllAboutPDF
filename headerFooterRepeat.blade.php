                        <li>
                            <span class="text-highlight">CARRIER’S Operating Authority and Compliance with Law</span>: CARRIER represents and warrants that it is
                            duly and legally qualified to provide, as a contract carrier, the transportation services contemplated
                            herein. CARRIER further represents and warrants that it does not have a conditional or
                            unsatisfactory safety rating issued from the U.S. Department of Transportation, and further agrees
                            to comply with all federal, state, and local laws regarding the provision of the transportation
                            services contemplated under this Agreement.
                        </li>
                        <li>
                            <span class="text-highlight">Performance of Services</span>: CARRIER agrees, subject to availability and legal loading capacity of
                            its equipment, to transport commodities and perform ancillary services tendered to it by AAA
